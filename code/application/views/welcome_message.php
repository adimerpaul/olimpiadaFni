                <h2>CRONOGRAMA DE CAPACITACIÓN </h2>
                <p>
                 La capacitación se llevará a cabo en los laboratorios de las carreras de Ingeniería de Sistemas e Ingeniería Informática ubicados en el Edificio 3 de Julio, calle Pagador entre Aldana y Ballivián.  
                </p>
                <p>
                  Los horarios y el Cronograma de Capacitación será publicado en la Web: <a href="http://www.sistemas.edu.bo/olimpiadaprog2017">http://www.sistemas.edu.bo/olimpiadaprog2017</a>. Los capacitadores serán competidores y ex-competidores de la ACM-ICPC (Competencia de Programación a nivel Nacional e Internacional).
                </p>
                <h2>VI.- DEL CONTENIDO TEMÁTICO</h2>
                <ul>
                  <li>
                    Nivel 1: Estructuras Secuenciales, Condicionales, Repetitivas, Vectores (Ordenación y Búsqueda).
                  </li>
                  <li>
                    Nivel 2: Todo el contenido de Nivel 1 y además: operaciones con Matrices y Strings.
                  </li>
                </ul>
                <h2>
                  VII. ACERCA DE LA OLIMPIADA
                </h2>
                <p>
                  La olimpiada se realizará en los laboratorios de las Carreras de Ingeniería de Sistemas e Ingeniería Informática, en la Ciudadela Universitaria de acuerdo al cronograma establecido. El mismo iniciará a las 9:30 de la mañana y la duración de la prueba será de TRES HORAS consecutivas, salvo que el Comité Organizador decida ampliar el plazo.
                </p>
                <p>
                  Durante la Olimpiada el Comité Organizador propondrá de 3 a 5 problemas, con distinto grado de dificultad. La resolución de los mismos se realizará en el lenguaje de programación C/C++, en el entorno  IDE CodeBlocks, utilizados en las principales competencias de programación como ser: OBI, IOI y la ACM-ICPC y Olimpiadas Científicas Plurinacionales de Bolivia.
                </p>
                <p>
                  Al finalizar el periodo de tiempo establecido comenzará el proceso de calificación, al final del cual se anunciarán los ganadores de la olimpiada.
                </p>
                <p>
                  El “Reglamento General de la Olimpiada” se proporcionará a los equipos durante la Capacitación.
                </p>
