                    <h1>VI OLIMPIADA DE PROGRAMACIÓN</h1>
                    <p class="lead">
                      PARA ESTUDIANTES DE SECUNDARIA 2017
                    </p>
                    <h2>PRESENTACIÓN</h2>
                    <p>
                      El desarrollo tecnológico del siglo XXI ha impactado notablemente en todo el quehacer humano; la humanidad ha tenido un avance acelerado y significativo en todos los niveles, llevando a nuestra civilización a un umbral sorprendentemente alto. Esto sin duda ha tenido mucho que ver con la creación de las computadoras. Actualmente se puede decir que el computador se usa en el trabajo, en la investigación, en la comunicación, en la exploración del espacio, en el suministro de recursos y tantas otras situaciones.
                    </p>
                    <p>
                      El software es uno de los elementos trascendentales de un computador, pues quizá es el aspecto que lo diferencia de las otras máquinas. El desarrollo de software es una de las actividades que ha llevado a muchos países a exportarlo y obtener ingresos importantes. Muchas de las empresas o corporaciones más rentables a lo largo del mundo son aquellas que trabajan en el rubro del software.  
                    </p>
                    <p>
                      La actividad de programación es un aspecto crucial en el desarrollo del software, fomentar a que nuestra gente pueda practicarla, es abrir las puertas a nuevas oportunidades de desarrollo regional y nacional; que mejor que hacerlo con nuestra niñez y juventud que son el presente y futuro de nuestra sociedad.
                    </p>
                    <p>
                      En ésta “VI Olimpiada de Programación para Estudiantes de Secundaria 2017” esperamos rescatar e incentivar talentos que actualmente son necesarios para una sociedad del siglo XXI.
                    </p>
                    <h2>BASES DE LA OLIMPIADA</h2>
                    <p>
                      Las Carreras de Ingeniería de Sistemas e Ingeniería Informática dependientes de la Facultad Nacional de Ingeniería de la Universidad Técnica de Oruro convocan a la “VI Olimpiada de Programación para Estudiantes de Secundaria 2017”, destinada a incentivar en los estudiantes la participación activa en esta área y desarrollar habilidades para la resolución de problemas mediante el uso de herramientas de programación.
                    </p>
                    <h2>I. OBJETIVO</h2>
                      <p>
                        El objetivo de la Olimpiada es reunir a estudiantes de Secundaria de la ciudad de Oruro para que adquieran nuevas experiencias, compartan sus conocimientos, demuestren sus habilidades y destrezas en el área de la programación, mediante la resolución de problemas.
                      </p>
                    <h2>II. CATEGORÍAS DE LA OLIMPIADA</h2>
                    <p>
                      La olimpiada se llevará a cabo en las siguientes categorías:
                      <ul>
                        <li> Nivel 1: Estudiantes de 3ro, 4to, y 5to de Secundaria.</li>
                        <li> Nivel 2: Estudiantes de 6to de Secundaria.</li>
                      </ul>
                    </p>
                    <h2>III. REQUISITOS PARA PARTICIPAR</h2>
                    <p>
                      Los requisitos que debe cumplir cada participante son los siguientes:
                    </p>
                    <ul>
                      <li>Ser estudiante matriculado en el Colegio al que representa, en el presente período académico.</li>
                      <li>
                        Ser parte de un equipo conformado por tres estudiantes, de acuerdo a lo establecido en el numeral II de CATEGORÍAS DE LA OLIMPIADA.
                      </li>
                      <li>
                        Un estudiante SOLO puede formar parte de UN equipo. 
                      </li>
                    </ul>
                    <h2>IV. INSCRIPCIONES</h2>
                    <p>
                      Los equipos podrán realizar una “Pre-inscripción” en la página Web de la carrera de Ingeniería de Sistemas e Informática:  <a href="http://www.sistemas.edu.bo/olimpiadaprog2017">http://www.sistemas.edu.bo/olimpiadaprog2017</a> donde se especificarán: 
                    </p>
                    <ol type="a">
                      <li>Los nombres, Cédulas de Identidad, Número de Celular y dirección de correo electrónico de los TRES miembros del equipo.</li>
                      <li>
                        Nombre completo y dirección de correo electrónico del Tutor(a): Profesor(a) de Computación o de otra asignatura.
                      </li>
                      <li>
                        Nombre del Colegio y Teléfono de contacto.
                      </li>
                      <li>
                        Turno (Tarde o mañana) en el que pasan las clases. 
                      </li>
                    </ol>
                    <p>
                      Cada equipo deberá imprimir la planilla de “Pre-inscripción” debidamente llenada y agregar la firma del Director(a) y sello del Colegio.
                    </p>
                    <p>
                      Para formalizar su “Inscripción” el (la) “Profesor(a) Tutor(a)”, deberá entregar la planilla de “Pre-inscripción” en las oficinas de la Carrera de Ingeniería de Sistemas e Ingeniería Informática (Ciudadela Universitaria). El monto de inscripción por equipo es de Bs.- 30 (Treinta bolivianos),  <b>pudiendo registrar cada colegio un máximo de 6 equipos por Nivel.</b>
                    </p>
